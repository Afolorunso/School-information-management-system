			<p class="des-contd">
				The development of science and technology constitutes a key part of the process of modernization. The more this development occurs locally, the better for the society concerned. In realization of this, various Governments and even corporate bodies have continued to make significant investments in the development of indigenous capacities in science and technology.<br/><br/>

				The establishment of Moshood Abiola (formerly Ogun State) Polytechnic was necessitated by the Federal Government white paper on National Policy on Education, which declared that: technical education forms the basis of technological development and as such, a greater proportion of educational expenditure would continually be devoted to technical education by government, both at the Federal and State levels. <br/><br/>

				Besides, the Ogun State Government believes that the development of technical manpower is one of the reasonable investments that a nation can embark upon in a programme of nation and society transformation. Thus, the Ogun State Polytechnic was established under the provisions of section 3 of the law cited as the Ogun State Polytechnic Law No. 8 of 12 June, 1980 and reviewed as the Moshood Abiola Polytechnic, Edict, 1999. <br/><br/>

				However, the first set of 220 students was admitted into the institution in January, 1979 with staff compliment of 25 academic and 5 senior administrative officers. As at 2004/2005 session, students’ population had risen to 18,878 with about 520 academic and non-academic staff compliment. <br/><br/>

				The Polytechnic commenced operation on two temporary campuses, namely: Oke-Egunya and Onikolobo. Movement of all departments from the temporary campuses to the permanent site at Ojere started in April 1985 and was completed in March, 1988. <br/><br/>

				The Ojere campus is situated in the South-Eastern part of Abeokuta, off old Owode road and some 4km from Ibara, in the capital of Ogun State of Nigeria. The site which covers about 960 hectares of land is located on an undulating topography with some marked relief features and series of valleys which provide good scenery. It is a well drained site bounded in the south by River Ogun. Thus, the area is endowed with rich natural beauty and history. <br/><br/>

				Arguably, the Polytechnic has a pride of place as one of the frontline tertiary institutions in the country today. This rating is not only in the area of academic attainments, but in the fields of research and practical contributions towards the technological development of Nigeria. Indeed, the institution has grown in leaps and bounds.
